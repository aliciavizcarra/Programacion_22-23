// Muestra los números de 320 al 160 contando de 20 en 20 hacia atrás. (utilizando while)

public class Ejemplo8 {

    public static void main(String[] args) {

        int numero = 320;

        while(numero >= 160 ){

            System.out.println(numero);
            numero -= 20;       // nuermo = numero - 20

        }
    }

}
