// Escribe un programa que calcule el perímetro del círculo.
// (Pi debe ser una constante)

public class Ejemplo3 {

    public static void main(String[] args) {

        final double PI = 3.14159;
        double radio = 45346.3464;
        double perimetro = 2*PI*radio;

        System.out.println("El círculo de radio " + radio + " tiene un perímetro de : "+ perimetro );

    }
}
